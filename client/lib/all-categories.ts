// Get URL-safe city name
export const getCitySlug = (city: string) =>
  city
    .toLowerCase()
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "");

// Get URL-safe category name
export const getCategorySlug = (category: string) =>
  category
    .toLowerCase()
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "");

// Generate all possible city-category combinations
export const getAllCombinations = () => {
  const combinations = [];

  // Add city-only pages
  allCities.forEach((city) => {
    combinations.push({
      type: "city",
      city,
      category: null,
      url: `/business/${getCitySlug(city)}`,
      title: `All Consultants in ${city}`,
      description: `Find visa and immigration consultants in ${city}`,
    });
  });

  // Add category-only pages
  allCategories.forEach((category) => {
    combinations.push({
      type: "category",
      city: null,
      category: category.name,
      categorySlug: category.slug,
      url: `/category/${category.slug}`,
      title: `${category.name} - All Cities`,
      description: category.description,
    });
  });

  // Add city-category combinations (48 categories × ~100 cities = ~4800 combinations)
  allCities.forEach((city) => {
    allCategories.forEach((category) => {
      combinations.push({
        type: "city-category",
        city,
        category: category.name,
        categorySlug: category.slug,
        url: `/business/${getCitySlug(city)}/${category.slug}`,
        title: `${category.name} in ${city}`,
        description: `Find ${category.name.toLowerCase()} in ${city}. ${category.description}`,
      });
    });
  });

  return combinations;
};

